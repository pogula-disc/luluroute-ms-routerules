package com.luluroute.ms.routerules.business.exceptions;

public class MappingFormatException extends MappingException {
	private static final long serialVersionUID = 1L;

	public MappingFormatException() {
		super();
	}

	public MappingFormatException(String message) {
		super(message);
	}

	public MappingFormatException(Throwable cause) {
		super(cause);
	}

	public MappingFormatException(String message, Throwable cause) {
		super(message, cause);
	}

}
